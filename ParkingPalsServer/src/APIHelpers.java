public class APIHelpers {

    public static double k2c(double inputTemp){
        double kelvinTemp = 0;
        return kelvinTemp = inputTemp - 273.15;
    }
    public  static double c2f(double inputTemp){
        double celsiusTemp = 0;
        return celsiusTemp = (inputTemp * 1.8) + 32;
    }
}
